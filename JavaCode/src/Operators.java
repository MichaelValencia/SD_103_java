public class Operators {
    public static void main(String[] args) {
        int x,z,n;
        x=10;
        z=10;
        n=10;
        x = x+1;
        System.out.println("x = " + x);
        z = z-1;
        System.out.println("z = " + z);
        n = n*-1;
        System.out.println("n = " + n);

        x=10;
        z=10;
        n =-n;
        x++;
        System.out.println("x = " + x);
        z--;
        System.out.println("z = " + z);
        n = -n;
        System.out.println("n = " + n);

        int age = 28;
        System.out.println(age+1);

    }
}
